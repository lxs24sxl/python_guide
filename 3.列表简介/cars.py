cars = ['bmw', 'audi', 'toyota', 'subaru']
cars.sort()
print(cars)

# 相反操作

cars = ['bmw', 'audi', 'toyota', 'subaru']
cars.sort(reverse=True)
print(cars)

# 临时排序 sorted()
cars = ['bmw', 'audi', 'toyota', 'subaru']
print("\nHere is sorted list:")
print(sorted(cars))


# reverse
cars = ['bmw', 'audi', 'toyota', 'subaru']
cars.reverse()
print(cars)

# len
print(len(cars))